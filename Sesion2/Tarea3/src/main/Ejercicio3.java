package main;

public class Ejercicio3 {

	public static void main(String[] args) {
		int x = 2 , y = 4;
		double n = 1.5, m = 2.5;
		
		System.out.println("VALORES");
		System.out.println("Valor de x es " + x);
		System.out.println("Valor de y es " + y);
		System.out.println("Valor de n es " + n);
		System.out.println("Valor de m es " + m + "\n");
		System.out.println("Suma de x + y es " + (x + y));
		System.out.println("Diferencia de x - y es " + (x - y));
		System.out.println("Producto de x * y es " + (x * y));
		System.out.println("Cociente de x / y es " + (x / y));
		System.out.println("Resto de x % y es " + (x % y));
		System.out.println("Suma de n + m es " + (n + m));
		System.out.println("Diferencia de n - m es " + (n - m));
		System.out.println("Producto de n * m es " + (n * m));
		System.out.println("Cociente de n / m es " + (n / m));
		System.out.println("Resto de n % m es " + (n % m));
		System.out.println("El doble de x es " + x * 2);
		System.out.println("El doble de y es " + y * 2);
		System.out.println("El doble de n es " + n * 2);
		System.out.println("El doble de m es " + m * 2);
		System.out.println("La suma de todas la variables es " + (x + y + n + m));
		System.out.println("El producto de todas las variables es " + (x * y * n * m));
	}

}
